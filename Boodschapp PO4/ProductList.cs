                    {
                        mProducts.Add(mConfirmedProducts[i]);
                    }

                    else
                    {
                        for (int j = 0; j < mProducts.Count; j++)
                        {
                            Comparing_products = string.Compare(mConfirmedProducts[i].name, mProducts[j].name, StringComparison.CurrentCulture);

